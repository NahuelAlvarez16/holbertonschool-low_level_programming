/**
 * op_add - sum a and b
 * @a: First number
 * @b: Second number
 * Return: return sum a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of a and b.
 * @a: First number
 * @b: Second number
 * Return: return difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplicate between a and b
 * @a: First number
 * @b: Second number
 * Return: return multiplicate between a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divison of a by b
 * @a: First number
 * @b: Second number
 * Return: return divison of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of a by b
 * @a: First number
 * @b: Second number
 * Return: return remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
